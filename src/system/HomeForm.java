    private void GRTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_GRTableMouseClicked
        // TODO add your handling code here:
        if(GRTable.getSelectedRowCount() != 0){
        String status =(String) GRTable.getValueAt(GRTable.getSelectedRow(), 4);
        
            
            switch(status){
                case "Rejected":
                    controlCB.setSelectedIndex(1);
                case "Pending":
                   controlCB.setSelectedIndex(2);
                   break;
                case "Granted":
                    controlCB.setSelectedIndex(0);
                    break;
            
            }
        
          
        
        }
    }//GEN-LAST:event_GRTableMouseClicked

